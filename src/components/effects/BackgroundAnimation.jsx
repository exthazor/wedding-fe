import React from 'react';

const BackgroundAnimation = ({ stars }) => {
  return (
    <div className="bg-animation">
      {stars.map(star => (
        <div
          key={star.id}
          className="star"
          style={{
            left: `${star.left}%`,
            top: `${star.top}%`,
            width: `${star.size}px`,
            height: `${star.size}px`,
            animationDelay: `${star.delay}s`
          }}
        />
      ))}
    </div>
  );
};

export default BackgroundAnimation;